function [X] = solve_discrete_triangular(T, Q)
% SOLVE_DISCRETE_TRIANGULAR Прямое решение дискретного уравнения Ляпунова.
%
% Данная функция решает уравнение вида: T' * X * T - X = -Q,
% где T — верхняя треугольная матрица (обычно из разложения Шура).
% Алгоритм основан на методе обратной подстановки для треугольных структур.
%
% Входные параметры:
%   T - Верхняя треугольная матрица (n x n)
%   Q - Матрица правой части (n x n)
%
% Выходные параметры:
%   X - Искомое решение уравнения Ляпунова

    n = size(T, 1);
    X = zeros(n, n);
    
    % Проход по строкам и столбцам для вычисления элементов X
    for i = 1:n
        for j = 1:n
            % Вычисление накопленной суммы для уже найденных элементов X(k,l).
            % Учитываются только k <= i и l <= j благодаря треугольной форме T.
            sum_val = 0;
            for k = 1:i
                for l = 1:j
                    if (k == i && l == j)
                        continue; 
                    end
                    % Суммирование вклада предыдущих компонентов
                    sum_val = sum_val + conj(T(k,i)) * X(k,l) * T(l,j);
                end
            end
            
            % Вычисление знаменателя: (conj(T_ii) * T_jj - 1).
            % Критически важно, чтобы собственные значения не находились на единичной окружности.
            denom = conj(T(i,i)) * T(j,j) - 1;
            
            % Проверка на вырожденность (близость к единичному кругу)
            if abs(denom) < 1e-18
                warning('Знаменатель близок к нулю: собственные значения вблизи единичного круга.');
            end
            
            % Нахождение текущего элемента решения
            X(i,j) = (-Q(i,j) - sum_val) / denom;
        end
    end
